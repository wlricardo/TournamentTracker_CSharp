namespace TrackerLibrary
{
    public class PersonModel
    {
        /// <summary>
        /// Indicates the player's first name
        /// </summary>
        public string FirstName { get; set; }

        /// <summary>
        /// Indicates the player's last name
        /// </summary>
        public string LastName { get; set; }
        
